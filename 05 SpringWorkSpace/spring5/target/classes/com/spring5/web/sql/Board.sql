/* 리스트 */
SELECT *
FROM (
	SELECT
		ROW_NUMBER() OVER(ORDER BY BNO DESC) AS NUM,
		A.*
	FROM
		BOARD A
)B
WHERE B.NUM BETWEEN 1 AND 5
ORDER BY B.NUM;
     


/* 아이디 리스트 */
SELECT *
FROM (
   SELECT
       ROW_NUMBER() OVER(ORDER BY BNO DESC) AS NUM,
       A.*
   FROM
       BOARD A
   WHERE writer LIKE 'danah'
)B
ORDER BY B.NUM;


